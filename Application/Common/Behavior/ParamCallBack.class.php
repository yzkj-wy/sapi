namespace Common\Behavior;
/**
 * 参数回调处理
 * @author 王强
 * @version 1.0.0
 */
class ParamCallBack
{
    /**
     * 数据
     * @var array
     */
    private $args = [];
    
    public function __construct(array $args)
    {
        $this->args = $args;
    }
    
    /**
     * 用户等级回调处理
     */
    public function gradeParam ()
    {
        $data = $this->args;
        
        unset($data[0]);
        
        return $data;
    }
}